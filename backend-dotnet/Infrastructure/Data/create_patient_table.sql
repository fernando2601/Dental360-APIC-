CREATE TABLE Patient (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    Name NVARCHAR(200) NOT NULL,
    Email NVARCHAR(200) NOT NULL,
    Phone NVARCHAR(50) NOT NULL,
    BirthDate DATETIME2 NULL,
    Gender NVARCHAR(20) NOT NULL,
    Address NVARCHAR(500) NOT NULL,
    MedicalHistory NVARCHAR(1000) NULL,
    Allergies NVARCHAR(500) NULL,
    EmergencyContact NVARCHAR(200) NULL,
    EmergencyPhone NVARCHAR(50) NULL,
    IsActive BIT NOT NULL DEFAULT 1,
    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(),
    UpdatedAt DATETIME2 NULL,
    CreatedByUserId INT NULL,
    UpdatedByUserId INT NULL,
    Nome NVARCHAR(200) NOT NULL,
    Idade INT NOT NULL,
    CPF NVARCHAR(20) NOT NULL,
    RG NVARCHAR(20) NULL,
    EstadoNascimento NVARCHAR(50) NOT NULL,
    DataNascimento DATETIME2 NOT NULL,
    Sexo NVARCHAR(20) NULL,
    Telefone NVARCHAR(50) NULL,
    Endereco NVARCHAR(500) NULL,
    City NVARCHAR(100) NOT NULL,
    State NVARCHAR(100) NOT NULL,
    ZipCode NVARCHAR(20) NOT NULL,
    Segment NVARCHAR(100) NOT NULL,
    FOREIGN KEY (CreatedByUserId) REFERENCES [User](Id),
    FOREIGN KEY (UpdatedByUserId) REFERENCES [User](Id)
); 